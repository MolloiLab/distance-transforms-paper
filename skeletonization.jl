# Define the kernel function for skeletonization
@kernel function skeletonize_kernel!(skeleton, dist)
	i, j = @index(Global, NTuple)
	rows, cols = size(skeleton)

	# Check if the current pixel is within the valid range (excluding border pixels)
	if i > 1 && i < rows && j > 1 && j < cols
		# Check if the current pixel has a distance value greater than 1
		if dist[i, j] > 1
			# Assume the current pixel is a local maxima initially
			is_maxima = true

			# Iterate over the 8-neighborhood of the current pixel
			for ni in i-1:i+1
				for nj in j-1:j+1
					# Skip the current pixel itself
					if ni == i && nj == j
						continue
					end
					# If any neighbor has a greater distance value, the current pixel is not a local maxima
					if dist[ni, nj] > dist[i, j]
						is_maxima = false
						break
					end
				end
				# If the current pixel is not a local maxima, exit the loop early
				if !is_maxima
					break
				end
