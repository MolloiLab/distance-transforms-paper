# ╔═╡ 0c197481-0cba-4ece-b62f-0c96381ced41
# Function to perform skeletonization using the distance transform on the GPU
function skeletonize_gpu!(skeleton, dist)
    # Create the kernel object
	backend = get_backend(dist)
    kernel! = skeletonize_kernel!(backend)

    # Launch the kernel
    kernel!(skeleton, dist, ndrange=size(dist))
end

