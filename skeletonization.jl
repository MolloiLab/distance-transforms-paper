# ╔═╡ a56889ee-c986-4660-a462-c14631e774bc
# Function to perform skeletonization utilizing the distance transform
function skeletonize_cpu!(skeleton, dist)
	rows, cols = size(skeleton)
	
	# Iterate over each pixel (excluding border pixels)
	for i in 2:rows-1
		for j in 2:cols-1
			# Check if the current pixel has a distance value greater than 1
			if dist[i, j] > 1
				# Assume the current pixel is a local maxima initially
				is_maxima = true
				
				# Iterate over the 8-neighborhood of the current pixel
				for ni in i-1:i+1
					for nj in j-1:j+1
						# Skip the current pixel itself
						if ni == i && nj == j
							continue
						end
						# If any neighbor has a greater distance value, the current pixel is not a local maxima
						if dist[ni, nj] > dist[i, j]
							is_maxima = false
							break
						end
					end
					# If the current pixel is not a local maxima, exit the loop early
					if !is_maxima
						break
					end
				end
				
				# If the current pixel is a local maxima, mark it as a skeleton pixel
				if is_maxima
					skeleton[i, j] = true
				end
			end
		end
	end
end

